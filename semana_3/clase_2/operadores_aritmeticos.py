"""
Operadores aritméticos

1. suma +
2. resta -
3. multiplicación *
4. división /
5. módulo %
6. exponenciación **
7. división entera //

"""

# 1. Suma +
a = 5 + 3
print("Suma:", a)  # Resultado: 8

# 2. Resta -
b = 10 - 4
print("Resta:", b)  # Resultado: 6

# 3. Multiplicación *
c = 7 * 6
print("Multiplicación:", c)  # Resultado: 42

# 4. División /
d = 20 / 4
print("División:", d)  # Resultado: 5.0

# 5. Módulo %
e = 10 % 3
print("Módulo:", e)  # Resultado: 1

# 6. Exponenciación **
f = 2 ** 3
print("Exponenciación:", f)  # Resultado: 8

# 7. División entera //
g = 17 // 3
print("División entera:", g)  # Resultado: 5
